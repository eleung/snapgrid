// Build-time syntax highlighting for the home page's hero code card.
//
// The home is a React component (not MDX), so it doesn't get Nextra's Shiki
// pipeline for free. We pre-highlight the snippet here with a dual light/dark
// theme (CSS-variable output that switches with the site's `.dark` class) and
// write it to a generated module the Home component renders. Runs via the
// `predev` / `prebuild` npm scripts, so it always stays in sync.

import { copyFileSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { codeToHtml } from "shiki";

// Two snippets for the home page's "30-second example" tabs: the drop-in
// <GridLayout> and the equivalent headless (provider + hooks) composition.
const DROPIN_SNIPPET = `import { GridLayout, useContainerWidth, type Layout } from "@snapgrid/react";
import { useState } from "react";

export function Board() {
  const { width, containerRef } = useContainerWidth();
  const [layout, setLayout] = useState<Layout>([
    { i: "a", x: 0, y: 0, w: 4, h: 2 },
    { i: "b", x: 4, y: 0, w: 4, h: 2 },
    { i: "c", x: 8, y: 0, w: 4, h: 2 },
  ]);

  return (
    <div ref={containerRef}>
      <GridLayout layout={layout} width={width} onLayoutChange={setLayout}>
        {layout.map((item) => (
          <div key={item.i} className="tile">{item.i}</div>
        ))}
      </GridLayout>
    </div>
  );
}`;

const HEADLESS_SNIPPET = `import {
  SnapGridProvider,
  useGridContainer,
  useGridItem,
  useGridPlaceholder,
  GridDragOverlay,
  useContainerWidth,
  type Layout,
} from "@snapgrid/react";
import { useState } from "react";

export function Board() {
  const { width, containerRef } = useContainerWidth();
  const [layout, setLayout] = useState<Layout>([
    { i: "a", x: 0, y: 0, w: 4, h: 2 },
    { i: "b", x: 4, y: 0, w: 4, h: 2 },
    { i: "c", x: 8, y: 0, w: 4, h: 2 },
  ]);

  return (
    <div ref={containerRef}>
      <SnapGridProvider layout={layout} width={width} onLayoutChange={setLayout}>
        <Surface items={layout} />
      </SnapGridProvider>
    </div>
  );
}

function Surface({ items }: { items: Layout }) {
  const { containerProps } = useGridContainer();
  const placeholder = useGridPlaceholder();

  return (
    <div {...containerProps}>
      {items.map((item) => (
        <Tile key={item.i} id={item.i} />
      ))}
      {placeholder && <div className="placeholder" style={placeholder.style} />}
      <GridDragOverlay>{(item) => <div className="tile">{item.i}</div>}</GridDragOverlay>
    </div>
  );
}

function Tile({ id }: { id: string }) {
  const { ref, style } = useGridItem(id);
  return (
    <div ref={ref} style={style} className="tile">
      {id}
    </div>
  );
}`;

const highlight = (code) =>
  codeToHtml(code, {
    lang: "tsx",
    themes: { light: "vitesse-light", dark: "vitesse-dark" },
    // Emit `--shiki-light` / `--shiki-dark` CSS vars instead of a baked color so
    // the same markup serves both themes (see .dg-codecard .shiki rules in CSS).
    defaultColor: false,
  });

const dropinHtml = await highlight(DROPIN_SNIPPET);
const headlessHtml = await highlight(HEADLESS_SNIPPET);

const outPath = fileURLToPath(new URL("../components/generated/hero-code.ts", import.meta.url));
mkdirSync(fileURLToPath(new URL("../components/generated/", import.meta.url)), { recursive: true });
writeFileSync(
  outPath,
  `// AUTO-GENERATED by scripts/highlight-snippets.mjs — do not edit by hand.\n// Regenerate with: pnpm --filter @snapgrid/docs highlight\nexport const HERO_DROPIN_HTML = ${JSON.stringify(dropinHtml)};\nexport const HERO_HEADLESS_HTML = ${JSON.stringify(headlessHtml)};\n`,
);

console.log(
  `highlighted hero snippets → ${outPath} (${dropinHtml.length}+${headlessHtml.length} bytes)`,
);

// Live-example sources: highlight each example component's *real file* so the
// /examples "Code" toggle shows exactly what runs — no hand-kept snippet to
// drift. Add an entry here per demo that opts into the toggle.
const EXAMPLES = [
  { id: "basic", file: "../components/examples/BasicExample.tsx" },
  { id: "compaction", file: "../components/examples/CompactorExample.tsx" },
  { id: "resize", file: "../components/examples/ResizeExample.tsx" },
  { id: "dragHandle", file: "../components/examples/DragHandleExample.tsx" },
  { id: "static", file: "../components/examples/StaticItemExample.tsx" },
  { id: "snap", file: "../components/examples/SnapExample.tsx" },
  { id: "responsive", file: "../components/examples/ResponsiveExample.tsx" },
  { id: "crossGrid", file: "../components/examples/CrossGridExample.tsx" },
  { id: "nested", file: "../components/examples/NestedExample.tsx" },
  { id: "externalDrop", file: "../components/examples/ExternalDropExample.tsx" },
  { id: "headless", file: "../components/examples/HeadlessExample.tsx" },
  { id: "notFound", file: "../components/examples/NotFoundExample.tsx" },
];
const exampleEntries = await Promise.all(
  EXAMPLES.map(async ({ id, file }) => {
    const raw = readFileSync(fileURLToPath(new URL(file, import.meta.url)), "utf8").trimEnd();
    return [id, { html: await highlight(raw), raw }];
  }),
);
const exampleOut = fileURLToPath(
  new URL("../components/generated/example-code.ts", import.meta.url),
);
writeFileSync(
  exampleOut,
  `// AUTO-GENERATED by scripts/highlight-snippets.mjs — do not edit by hand.\n// Regenerate with: pnpm --filter @snapgrid/docs highlight\nexport const EXAMPLE_CODE = ${JSON.stringify(Object.fromEntries(exampleEntries))} as const;\n`,
);
console.log(`highlighted ${exampleEntries.length} example source(s) → ${exampleOut}`);

// Mirror the repo-root changelog into the app so /changelog can import it.
// (Next forbids importing files outside the working directory; this keeps a
// single source of truth — the root CHANGELOG.md — without a fragile symlink.)
const changelogOut = fileURLToPath(
  new URL("../components/generated/changelog.md", import.meta.url),
);
copyFileSync(fileURLToPath(new URL("../../../CHANGELOG.md", import.meta.url)), changelogOut);
console.log(`mirrored changelog → ${changelogOut}`);

// Emit the published package version for the header badge (reads @snapgrid/react,
// the package users install). Auto-updates when that version is bumped.
const pkgPath = fileURLToPath(
  new URL("../../../packages/grid-react/package.json", import.meta.url),
);
const { version } = JSON.parse(readFileSync(pkgPath, "utf8"));
const versionOut = fileURLToPath(new URL("../components/generated/version.ts", import.meta.url));
writeFileSync(
  versionOut,
  `// AUTO-GENERATED by scripts/highlight-snippets.mjs — do not edit by hand.\nexport const VERSION = ${JSON.stringify(version)};\n`,
);
console.log(`emitted version ${version} → ${versionOut}`);
